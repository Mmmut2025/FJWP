class Operator{
	public static void main(String args[]){
		int a = 10;

		a = 10;
		System.out.println("value of a is :" + a);

		a += 10;
		System.out.println("value of a is :" + a);

		a -= 5;
		System.out.println("value of a is :" + a);

		a *= 10;
		System.out.println("value of a is :" + a);

		a /= 2;
		System.out.println("value of a is :" + a);

		a %= 3;
		System.out.println("value of a is :" + a);


	}
}